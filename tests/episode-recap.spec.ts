test.describe('Episode Recap — Settings toggle', () => {
  test.beforeEach(async ({ request }) => {
    await clearLibrary(request);
    await seedSeries(request, { status: 'watching' });
    await watchEpisode(request, 1);
    // Reset episode recap setting to its default (enabled) before each test
    await request.put('/api/settings', {
      data: { episodeRecapEnabled: true },
    });
  });

  test('Episode Recap toggle is visible in the Preferences section', async ({ page }) => {
    await setupTMDBMocks(page);
    await page.goto('/settings');
    await expect(
      page.locator('.flex.items-center.justify-between').filter({ hasText: 'Episode Recap' }),
    ).toBeVisible();
  });

  test('Episode Recap toggle is on by default', async ({ page }) => {
    await setupTMDBMocks(page);
    await page.goto('/settings', { waitUntil: 'networkidle' });
    const toggle = page
      .locator('.flex.items-center.justify-between')
      .filter({ hasText: 'Episode Recap' })
      .getByRole('switch');
    await expect(toggle).toHaveAttribute('aria-checked', 'true');
  });

  test('disabling Episode Recap hides the recap button on the series page', async ({
    page,
    request,
  }) => {
    // Disable via API so the setting is persisted before page load
    await request.put('/api/settings', { data: { episodeRecapEnabled: false } });
    await goToEpisodesTab(page);
    await expect(page.getByRole('button', { name: 'Recap' })).not.toBeVisible();
  });

  test('re-enabling Episode Recap shows the recap button again', async ({ page, request }) => {
    await request.put('/api/settings', { data: { episodeRecapEnabled: false } });
    await goToEpisodesTab(page);
    await expect(page.getByRole('button', { name: 'Recap' })).not.toBeVisible();

    // Re-enable via settings page
    await page.goto('/settings', { waitUntil: 'networkidle' });
    const toggle = page
      .locator('.flex.items-center.justify-between')
      .filter({ hasText: 'Episode Recap' })
      .getByRole('switch');
    await toggle.click();
    await expect(toggle).toHaveAttribute('aria-checked', 'true');

    // Navigate back and confirm the button is back
    await goToEpisodesTab(page);
    await expect(page.getByRole('button', { name: 'Recap' })).toBeVisible();
  });

  test('toggling Episode Recap off in settings persists after re-navigation', async ({
    page,
    request,
  }) => {
    await request.put('/api/settings', { data: { episodeRecapEnabled: false } });
    // First visit — no button
    await goToEpisodesTab(page);
    await expect(page.getByRole('button', { name: 'Recap' })).not.toBeVisible();
    // Navigate away and back
    await page.goto('/library');
    await goToEpisodesTab(page);
    await expect(page.getByRole('button', { name: 'Recap' })).not.toBeVisible();
  });
});
